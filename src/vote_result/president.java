    private void displayVotes() {
        String filePath = "users" + File.separator + "vote_result.txt";
        int[] votes = countVotes(filePath);
        total_vote.setText("Total Votes: " + votes[0]);
        jLabel4.setText("Total Votes: " + votes[1]);
    }

    private int[] countVotes(String filePath) {
        int luffyVotes = 0;
        int gokuVotes = 0;
        try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = reader.readLine()) != null) {
                if (line.contains("President Luffy")) {
                    luffyVotes++;
                } 
                if (line.contains("President Goku")) {
                    gokuVotes++;
                }
            }
        } catch (IOException e) {
            e.printStackTrace(); // Handle file reading errors here
        }
        return new int[] { luffyVotes, gokuVotes };
    }
    

